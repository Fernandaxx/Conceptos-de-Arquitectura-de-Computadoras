ORG 1000H
    NUM DB 00000101b
    CANTROT DB 4H
    RES DB ?
ORG 3000H
ROTARIZ: CMP CL, 0
    JZ ROTARIZ_FIN
    ADD AL, AL
    ADC AL, 0
    DEC CL
    JMP ROTARIZ
ROTARIZ_FIN:
    MOV RES, AL
    ;MOV [BX], AL
    RET

ORG 2000H
    MOV AL, NUM
    MOV CL, CANTROT
    MOV BX, OFFSET RES
    CALL ROTARIZ
    HLT
    END
