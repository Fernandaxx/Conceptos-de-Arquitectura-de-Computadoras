ORG 1000H
PATRON DB 11000011b
CB EQU 33H
PB EQU 31H
CA EQU 32H
PA EQU 30H
MSJ1 DB "llave prendida"
MSJ2 DB "llave apagada"
FIN DB ?


ORG 2000H 

;CONFIG LUCES, TODAS 0 SALIDA
MOV AL, 00H
OUT CB, AL

; ENCENDER LUCES
MOV AL, PATRON
OUT PB, AL

;CONFIG LLAVES, TODAS 1 SALIDA
MOV AL, 0FFH
OUT CA, AL
IN AL,PA


CMP AL,10000000b
JS APAGADA
MOV BX, OFFSET MSJ1
MOV AL, OFFSET MSJ2 - OFFSET MSJ1
INT 7
JMP FINAL
APAGADA: MOV BX, OFFSET MSJ2
MOV AL, OFFSET FIN - OFFSET MSJ2
INT 7

FINAL:
INT 0
END 
